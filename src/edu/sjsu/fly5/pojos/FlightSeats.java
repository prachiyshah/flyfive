package edu.sjsu.fly5.pojos;

public class FlightSeats {
	String travelClass;
	float seatRatio;

	public String getTravelClass() {
		return travelClass;
	}
	public void setTravelClass(String travelClass) {
		this.travelClass = travelClass;
	}
	public float getSeatRatio() {
		return seatRatio;
	}
	public void setSeatRatio(float seatRatio) {
		this.seatRatio = seatRatio;
	}
}
